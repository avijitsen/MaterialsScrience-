<p class="text">The study of MAX phases has become an important sub-branch in materials science and technology due to their interesting properties and various studies since their discovery during 1960s. MAX phases, where M is an early transition metal, A is a group IIIA or IVA element, and X is either C or N, attract more and more attention due to their unusual properties associated metals and ceramics. Like metals, they are electrically and thermally conductive, not susceptible to thermal shock, plastic at high temperature and exceptionally damage tolerant, and most readily machinable. Like ceramics, they are elastically rigid, lightweight, creep and fatigue resistant and maintain their strengths to high temperatures. This makes them attractive for many applications such as structural materials at elevated temperature. We have studied materials properties such as structural, electrical, optical, thermodynamical etc using the density functional theory (DFT) as implemented in the CASTEP code.
